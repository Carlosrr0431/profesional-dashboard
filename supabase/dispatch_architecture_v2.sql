-- =============================================================
-- Dispatch Architecture V2 (DB-first, idempotent, lock-safe)
-- =============================================================
-- Objetivo:
-- - Llevar la cola, locks, retries y timeouts a Postgres/Supabase.
-- - Evitar doble asignacion/race conditions en workers serverless.
-- - Habilitar idempotencia por event_id/source_event_key.
--
-- Este script es incremental (CREATE IF NOT EXISTS / ALTER IF NOT EXISTS).
-- Aplicar manualmente en Supabase SQL Editor.

