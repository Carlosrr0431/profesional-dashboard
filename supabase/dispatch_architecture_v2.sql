UPDATE public.trips
SET dispatch_status = CASE
  WHEN status = 'queued' THEN 'queued'::public.dispatch_status_enum
  WHEN status = 'pending' THEN 'waiting_acceptance'::public.dispatch_status_enum
  WHEN status IN ('accepted', 'going_to_pickup', 'in_progress') THEN 'accepted'::public.dispatch_status_enum
  WHEN status = 'completed' THEN 'completed'::public.dispatch_status_enum
  WHEN status = 'cancelled' THEN 'cancelled'::public.dispatch_status_enum
  ELSE dispatch_status
END
WHERE dispatch_status = 'idle';

