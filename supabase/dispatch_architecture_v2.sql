CREATE OR REPLACE FUNCTION public.acquire_worker_lock(
  p_lock_key TEXT,
  p_owner TEXT,
  p_ttl_seconds INTEGER DEFAULT 30
)
RETURNS TABLE (
  acquired BOOLEAN,
  lock_token UUID,
  expires_at TIMESTAMPTZ
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_now TIMESTAMPTZ := NOW();
  v_token UUID := gen_random_uuid();
  v_expires TIMESTAMPTZ := v_now + make_interval(secs => GREATEST(5, COALESCE(p_ttl_seconds, 30)));
BEGIN
  IF p_lock_key IS NULL OR btrim(p_lock_key) = '' THEN
    RETURN QUERY SELECT FALSE, NULL::UUID, NULL::TIMESTAMPTZ;
    RETURN;
  END IF;

  DELETE FROM public.worker_locks
  WHERE lock_key = p_lock_key
    AND expires_at <= v_now;

  INSERT INTO public.worker_locks (lock_key, owner, lock_token, acquired_at, expires_at)
  VALUES (p_lock_key, COALESCE(NULLIF(p_owner, ''), 'worker'), v_token, v_now, v_expires)
  ON CONFLICT (lock_key) DO NOTHING;

  IF FOUND THEN
    RETURN QUERY SELECT TRUE, v_token, v_expires;
  ELSE
    RETURN QUERY SELECT FALSE, NULL::UUID, NULL::TIMESTAMPTZ;
  END IF;
END;
$$;

CREATE OR REPLACE FUNCTION public.release_worker_lock(
  p_lock_key TEXT,
  p_lock_token UUID
)
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  DELETE FROM public.worker_locks
  WHERE lock_key = p_lock_key
    AND lock_token = p_lock_token;
  RETURN FOUND;
END;
$$;

-- -----------------------------------------------------------------
-- 9) Locking de dispatch con FOR UPDATE SKIP LOCKED
-- -----------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.claim_dispatch_queue_batch(
  p_worker TEXT,
  p_limit INTEGER DEFAULT 20,
  p_lock_seconds INTEGER DEFAULT 25
)
RETURNS TABLE (
  trip_id UUID,
  lock_token UUID,
  attempt_no INTEGER,
  passenger_phone TEXT,
  destination_lat DOUBLE PRECISION,
  destination_lng DOUBLE PRECISION,
  enqueued_at TIMESTAMPTZ
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_now TIMESTAMPTZ := NOW();
  v_limit INTEGER := GREATEST(1, COALESCE(p_limit, 20));
  v_lock_seconds INTEGER := GREATEST(10, COALESCE(p_lock_seconds, 25));
BEGIN
  -- Recupera claims vencidos (worker muerto/redeploy serverless).
  UPDATE public.dispatch_queue
  SET
    queue_status = 'queued',
    lock_token = NULL,
    lock_owner = NULL,
    lock_acquired_at = NULL,
    lock_expires_at = NULL,
    next_attempt_at = LEAST(next_attempt_at, v_now),
    updated_at = v_now
  WHERE queue_status = 'claimed'
    AND lock_expires_at IS NOT NULL
    AND lock_expires_at <= v_now;

  RETURN QUERY
  WITH candidates AS (
    SELECT dq.trip_id
    FROM public.dispatch_queue dq
    JOIN public.trips t ON t.id = dq.trip_id
    WHERE dq.queue_status = 'queued'
      AND dq.next_attempt_at <= v_now
      AND dq.attempts_count < dq.max_attempts
      AND t.status = 'queued'
    ORDER BY dq.priority ASC, dq.enqueued_at ASC
    FOR UPDATE SKIP LOCKED
    LIMIT v_limit
  ),
  claimed AS (
    UPDATE public.dispatch_queue dq
    SET
      queue_status = 'claimed',
      lock_token = gen_random_uuid(),
      lock_owner = COALESCE(NULLIF(p_worker, ''), 'worker'),
      lock_acquired_at = v_now,
      lock_expires_at = v_now + make_interval(secs => v_lock_seconds),
      attempts_count = dq.attempts_count + 1,
      last_attempt_at = v_now,
      updated_at = v_now
    FROM candidates c
    WHERE dq.trip_id = c.trip_id
    RETURNING dq.trip_id, dq.lock_token, dq.attempts_count, dq.enqueued_at
  )
  SELECT
    c.trip_id,
    c.lock_token,
    c.attempts_count AS attempt_no,
    t.passenger_phone,
    t.destination_lat::double precision,
    t.destination_lng::double precision,
    c.enqueued_at
  FROM claimed c
  JOIN public.trips t ON t.id = c.trip_id;
END;
$$;

CREATE OR REPLACE FUNCTION public.release_dispatch_claim(
  p_trip_id UUID,
  p_lock_token UUID,
  p_result TEXT DEFAULT 'retry',
  p_retry_seconds INTEGER DEFAULT 12,
  p_error_code TEXT DEFAULT NULL,
  p_error_text TEXT DEFAULT NULL,
  p_selected_driver UUID DEFAULT NULL,
  p_selected_distance_km NUMERIC DEFAULT NULL,
  p_selected_score NUMERIC DEFAULT NULL
)
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_result TEXT := LOWER(COALESCE(p_result, 'retry'));
  v_retry_seconds INTEGER := GREATEST(1, COALESCE(p_retry_seconds, 12));
BEGIN
  IF p_trip_id IS NULL OR p_lock_token IS NULL THEN
    RETURN FALSE;
  END IF;

  IF v_result = 'retry' THEN
    UPDATE public.dispatch_queue
    SET
      lock_token = NULL,
      lock_owner = NULL,
      lock_acquired_at = NULL,
      lock_expires_at = NULL,
      next_attempt_at = NOW() + make_interval(secs => v_retry_seconds),
      selected_driver_id = COALESCE(p_selected_driver, selected_driver_id),
      selected_distance_km = COALESCE(p_selected_distance_km, selected_distance_km),
      selected_score = COALESCE(p_selected_score, selected_score),
      last_error_code = NULLIF(p_error_code, ''),
      last_error = NULLIF(p_error_text, ''),
      queue_status = CASE
        WHEN attempts_count >= max_attempts THEN 'dead_letter'::public.dispatch_status_enum
        ELSE 'queued'::public.dispatch_status_enum
      END,
      updated_at = NOW()
    WHERE trip_id = p_trip_id
      AND lock_token = p_lock_token
      AND queue_status = 'claimed';

    RETURN FOUND;
  ELSIF v_result IN ('done', 'accepted', 'cancelled', 'drop') THEN
    DELETE FROM public.dispatch_queue
    WHERE trip_id = p_trip_id
      AND lock_token = p_lock_token
      AND queue_status = 'claimed';
    RETURN FOUND;
  ELSIF v_result = 'dead' THEN
    UPDATE public.dispatch_queue
    SET
      queue_status = 'dead_letter',
      lock_token = NULL,
      lock_owner = NULL,
      lock_acquired_at = NULL,
      lock_expires_at = NULL,
      last_error_code = COALESCE(NULLIF(p_error_code, ''), 'dead_letter'),
      last_error = NULLIF(p_error_text, ''),
      updated_at = NOW()
    WHERE trip_id = p_trip_id
      AND lock_token = p_lock_token
      AND queue_status = 'claimed';
    RETURN FOUND;
  END IF;

  RETURN FALSE;
END;
$$;

