CREATE OR REPLACE FUNCTION public.claim_trip_queue_item(
  p_trip_id UUID,
  p_worker TEXT DEFAULT NULL,
  p_lock_seconds INTEGER DEFAULT 25
)
RETURNS TABLE (
  claimed BOOLEAN,
  lock_token UUID,
  reason TEXT
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_now TIMESTAMPTZ := NOW();
  v_lock_token UUID;
BEGIN
  IF p_trip_id IS NULL THEN
    RETURN QUERY SELECT FALSE, NULL::UUID, 'missing_trip_id';
    RETURN;
  END IF;

  UPDATE public.dispatch_queue
  SET
    queue_status = 'queued',
    lock_token = NULL,
    lock_owner = NULL,
    lock_acquired_at = NULL,
    lock_expires_at = NULL,
    updated_at = v_now
  WHERE trip_id = p_trip_id
    AND queue_status = 'claimed'
    AND lock_expires_at IS NOT NULL
    AND lock_expires_at <= v_now;

  UPDATE public.dispatch_queue
  SET
    queue_status = 'claimed',
    lock_token = gen_random_uuid(),
    lock_owner = COALESCE(NULLIF(p_worker, ''), 'worker'),
    lock_acquired_at = v_now,
    lock_expires_at = v_now + make_interval(secs => GREATEST(10, COALESCE(p_lock_seconds, 25))),
    attempts_count = attempts_count + 1,
    last_attempt_at = v_now,
    updated_at = v_now
  WHERE trip_id = p_trip_id
    AND queue_status = 'queued'
    AND next_attempt_at <= v_now
  RETURNING dispatch_queue.lock_token INTO v_lock_token;

  IF v_lock_token IS NULL THEN
    RETURN QUERY SELECT FALSE, NULL::UUID, 'not_available';
    RETURN;
  END IF;

  RETURN QUERY SELECT TRUE, v_lock_token, 'claimed';
END;
$$;

CREATE OR REPLACE FUNCTION public.release_trip_queue_item(
  p_trip_id UUID,
  p_lock_token UUID,
  p_result TEXT DEFAULT 'retry',
  p_retry_seconds INTEGER DEFAULT 12,
  p_error TEXT DEFAULT NULL
)
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  RETURN public.release_dispatch_claim(
    p_trip_id,
    p_lock_token,
    p_result,
    p_retry_seconds,
    NULL,
    p_error,
    NULL,
    NULL,
    NULL
  );
END;
$$;

