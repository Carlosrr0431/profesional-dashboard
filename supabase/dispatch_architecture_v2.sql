CREATE OR REPLACE FUNCTION public.append_trip_event_once(
  p_source TEXT,
  p_source_event_key TEXT,
  p_trip_id UUID,
  p_event_type public.trip_event_type_enum,
  p_payload JSONB DEFAULT '{}'::jsonb
)
RETURNS TABLE (
  inserted BOOLEAN,
  event_row_id BIGINT,
  event_uuid UUID
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  IF p_source IS NULL OR btrim(p_source) = '' THEN
    RAISE EXCEPTION 'source is required';
  END IF;

  RETURN QUERY
  WITH ins AS (
    INSERT INTO public.trip_events (
      source,
      source_event_key,
      trip_id,
      event_type,
      payload
    )
    VALUES (
      p_source,
      NULLIF(p_source_event_key, ''),
      p_trip_id,
      p_event_type,
      COALESCE(p_payload, '{}'::jsonb)
    )
    ON CONFLICT (source, source_event_key) DO NOTHING
    RETURNING id, event_id
  )
  SELECT TRUE, ins.id, ins.event_id
  FROM ins
  UNION ALL
  SELECT FALSE, e.id, e.event_id
  FROM public.trip_events e
  WHERE e.source = p_source
    AND e.source_event_key = NULLIF(p_source_event_key, '')
    AND NOT EXISTS (SELECT 1 FROM ins)
  LIMIT 1;

  IF p_source_event_key IS NOT NULL AND btrim(p_source_event_key) <> '' THEN
    UPDATE public.trip_events
    SET duplicate_count = duplicate_count + 1,
        updated_at = NOW()
    WHERE source = p_source
      AND source_event_key = p_source_event_key
      AND id NOT IN (
        SELECT id
        FROM public.trip_events
        WHERE source = p_source
          AND source_event_key = p_source_event_key
        ORDER BY created_at ASC
        LIMIT 1
      );
  END IF;
END;
$$;

